# 시키고 싶은 일 : 숫자, 글자 데이터를 화면에 출력하기
# 출력하기 => print( 데이터 )
# 숫자 데이터 출력 => 정수(int)
print(2024)
print(1227)
print()

# 숫자 데이터 출력 => 실수(float)
print(2.9)
print(5.1230)
print(126.989)
print(1.)
print()

# 문자/글자/문자열 데이터 출력 => 글자(str)
# '데이터' / "데이터"
print("홍길동")
print("지금은 겨울")
print("Happy New Year")
print("2024")

